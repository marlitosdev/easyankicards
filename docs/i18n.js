let LANG = localStorage.getItem("eac_lang");
if (!LANG) {
  LANG = (navigator.language || "en").toLowerCase().startsWith("pt") ? "pt" : "en";
}
if (!UI[LANG]) LANG = "en";
function setLanguage(l) { if (UI[l]) { LANG = l; localStorage.setItem("eac_lang", l); } }
function t(key, params) {
  let s = (UI[LANG] && UI[LANG][key]) || key;
  if (params) for (const k in params) s = s.split("{" + k + "}").join(params[k]);
  return s;
}
